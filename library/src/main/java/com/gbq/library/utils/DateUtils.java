    /**
     * 获取当月有多少天。
     *
     * @param year
     * @param month
     * @return
     */
    public static int dayOfMonth(int year, int month) {
        int day;
        if (DateUtils.isMonthOf31(month)) {
            day = 31;
        } else if (month == 2) {
            if (DateUtils.isLeapYear(year)) {
                day = 29;
            } else {
                day = 28;
            }
        } else {
            day = 30;
        }
        return day;
    }

    /**
     * 获取当月第一天是星期几（0为星期日）
     *
     * @param year
     * @param month
     * @return
     */
    public static int getWeekOfMonthOfOne(int year, int month) {

        String date = null;

        if (month < 10) {
            date = year + "-0" + month + "-01";
        } else {
            date = year + "-" + month + "-01";
        }

        Week week = getWeek(date);

        if (week == Week.SUNDAY) {
            return 0;
        } else if (week == Week.MONDAY) {
            return 1;
        } else if (week == Week.TUESDAY) {
            return 2;
        } else if (week == Week.WEDNESDAY) {
            return 3;
        } else if (week == Week.THURSDAY) {
            return 4;
        } else if (week == Week.FRIDAY) {
            return 5;
        } else if (week == Week.SATURDAY) {
            return 6;
        }
        return 0;
    }

